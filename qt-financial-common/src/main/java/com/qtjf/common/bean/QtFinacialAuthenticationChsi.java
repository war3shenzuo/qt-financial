package com.qtjf.common.bean;

public class QtFinacialAuthenticationChsi {
	private String id;
	private String chsiCode;
	private String chsiPass;

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getChsiCode() {
		return chsiCode;
	}

	public void setChsiCode(String chsiCode) {
		this.chsiCode = chsiCode;
	}

	public String getChsiPass() {
		return chsiPass;
	}

	public void setChsiPass(String chsiPass) {
		this.chsiPass = chsiPass;
	}
}
