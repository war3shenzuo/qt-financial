package com.qtjf.tpa.tanzhi.mobile.report.resultdetailinfo.callrecord;

public class CallBucketInfo {
	private String bucket_name;
	private Integer callers_number;
	private Integer call_count;
	private Integer call_duration;
	public String getBucket_name() {
		return bucket_name;
	}
	public void setBucket_name(String bucket_name) {
		this.bucket_name = bucket_name;
	}
	public Integer getCallers_number() {
		return callers_number;
	}
	public void setCallers_number(Integer callers_number) {
		this.callers_number = callers_number;
	}
	public Integer getCall_count() {
		return call_count;
	}
	public void setCall_count(Integer call_count) {
		this.call_count = call_count;
	}
	public Integer getCall_duration() {
		return call_duration;
	}
	public void setCall_duration(Integer call_duration) {
		this.call_duration = call_duration;
	}
}
