package com.qtjf.tpa.tanzhi.mobile.report;

public class CommunicationDetection {
	private String the_last_silent_duration_time;
	private Integer silent_count;
	private String night_activities;
	private String average_silent_duration_time;
	private String the_last_call_time;
	public String getThe_last_silent_duration_time() {
		return the_last_silent_duration_time;
	}
	public void setThe_last_silent_duration_time(String the_last_silent_duration_time) {
		this.the_last_silent_duration_time = the_last_silent_duration_time;
	}
	public Integer getSilent_count() {
		return silent_count;
	}
	public void setSilent_count(Integer silent_count) {
		this.silent_count = silent_count;
	}
	public String getNight_activities() {
		return night_activities;
	}
	public void setNight_activities(String night_activities) {
		this.night_activities = night_activities;
	}
	public String getAverage_silent_duration_time() {
		return average_silent_duration_time;
	}
	public void setAverage_silent_duration_time(String average_silent_duration_time) {
		this.average_silent_duration_time = average_silent_duration_time;
	}
	public String getThe_last_call_time() {
		return the_last_call_time;
	}
	public void setThe_last_call_time(String the_last_call_time) {
		this.the_last_call_time = the_last_call_time;
	}
	
}
