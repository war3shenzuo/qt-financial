package com.qtjf.tpa.tanzhi.mobile.report.resultdetailinfo.smsrecord;

public class SmsBucketInfo {
	private String bucket_name;
	private Integer sender_receiver_number;
	private Integer send_receive_count;
	public String getBucket_name() {
		return bucket_name;
	}
	public void setBucket_name(String bucket_name) {
		this.bucket_name = bucket_name;
	}
	public Integer getSender_receiver_number() {
		return sender_receiver_number;
	}
	public void setSender_receiver_number(Integer sender_receiver_number) {
		this.sender_receiver_number = sender_receiver_number;
	}
	public Integer getSend_receive_count() {
		return send_receive_count;
	}
	public void setSend_receive_count(Integer send_receive_count) {
		this.send_receive_count = send_receive_count;
	}

}
