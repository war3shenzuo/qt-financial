package com.qtjf.tpa.tanzhi.mobile.report;

public class RechargeInfo {

	private String recharge_time;
	private Integer recharge_count;
	private Integer recharge_amount;
	private Integer recharge_max;
	public String getRecharge_time() {
		return recharge_time;
	}
	public void setRecharge_time(String recharge_time) {
		this.recharge_time = recharge_time;
	}
	public Integer getRecharge_count() {
		return recharge_count;
	}
	public void setRecharge_count(Integer recharge_count) {
		this.recharge_count = recharge_count;
	}
	public Integer getRecharge_amount() {
		return recharge_amount;
	}
	public void setRecharge_amount(Integer recharge_amount) {
		this.recharge_amount = recharge_amount;
	}
	public Integer getRecharge_max() {
		return recharge_max;
	}
	public void setRecharge_max(Integer recharge_max) {
		this.recharge_max = recharge_max;
	}
}
